public class Ejercicio2_5 {
/*
Ejercicio 2.5 Escribir un programa para declarar los siguientes
caracteres mediante su posición en la tabla ASCII e imprimirlos en
pantalla.
➢ {
➢ ]
➢ C
➢ @
 */
    public static void main(String[] args) {
        char a = 123;// {
        char b = 93;// ]
        char c = 67;// C
        char d = 64;// @
        
        System.out.println("El resultado de 'a' es: "+a);
        System.out.println("El resultado de 'b' es: "+b);
        System.out.println("El resultado de 'c' es: "+c);
        System.out.println("El resultado de 'd' es: "+d);
    }
}
